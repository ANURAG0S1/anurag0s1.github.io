package me.anuragkush.college;

public class switch_if {
    public static void main(String[] args) {
        int num =0;
        switch(num){
            case 0:
                System.out.println(num);
break;
            case 2:
                System.out.println( "case 2" + num);
break;
            default:
                System.out.println("default" + num);
        }
    }
}
