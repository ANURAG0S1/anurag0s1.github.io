function add() {
    let Numbe = document.querySelector('#number').innerHTML;
    let NewNumber = Number(Numbe) + 1;
    Numbe = document.querySelector('#number').innerHTML = NewNumber;

    ;

}
function sub() {
    let Numbe = document.querySelector('#number').innerHTML;
    let NewNumber = Number(Numbe) - 1;
    Numbe = document.querySelector('#number').innerHTML = NewNumber;

    ;

}
function reset() {
    let Numbe = document.querySelector('#number').innerHTML;
    let NewNumber =  0;
    Numbe = document.querySelector('#number').innerHTML = NewNumber;

    ;

}

 